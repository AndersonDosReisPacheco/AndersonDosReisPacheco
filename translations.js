// Language selector functionality
let currentLanguage = "pt-BR";

function changeLanguage(lang) {
  if (!translations[lang]) return;

  currentLanguage = lang;
  updateAllTexts();
  saveLanguagePreference(lang);
  updateActiveLanguageButton();

  // Update URL for sharing
  updateURLWithLanguage();
}

function updateAllTexts() {
  const langData = translations[currentLanguage];

  if (!langData) return;

  // 1. Update all elements with data-i18n attribute
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (langData[key]) {
      element.textContent = langData[key];
    }
  });

  // 2. Update all placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    if (langData[key]) {
      element.placeholder = langData[key];
    }
  });

  // 3. Update all title attributes
  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const key = element.getAttribute("data-i18n-title");
    if (langData[key]) {
      element.title = langData[key];
    }
  });

  // 4. Update all alt attributes
  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.getAttribute("data-i18n-alt");
    if (langData[key]) {
      element.alt = langData[key];
    }
  });

  // 5. Update all aria-label attributes
  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.getAttribute("data-i18n-aria-label");
    if (langData[key]) {
      element.setAttribute("aria-label", langData[key]);
    }
  });

  // 6. Update dynamic content (projects, skills, etc.)
  updateDynamicContent(langData);

  // 7. Update page title and meta
  updatePageMeta(langData);

  console.log(`🌐 Idioma alterado para: ${currentLanguage}`);
}

function updateDynamicContent(langData) {
  // Update projects filter buttons
  const filterButtons = document.querySelectorAll(".filter-btn");
  filterButtons.forEach((btn) => {
    const filter = btn.getAttribute("data-filter");
    const key = {
      all: "all_projects",
      java: "java_projects",
      web: "web_projects",
      fullstack: "fullstack",
      pessoal: "personal",
    }[filter];

    if (key && langData[key]) {
      // Preserve count in parentheses
      const currentText = btn.textContent;
      const countMatch = currentText.match(/\((\d+)\)/);
      if (countMatch) {
        btn.textContent = `${langData[key]} (${countMatch[1]})`;
      } else {
        btn.textContent = langData[key];
      }
    }
  });

  // Update project cards
  updateProjectCardsContent(langData);

  // Update skills categories
  updateSkillsContent(langData);

  // Update competencies lists
  updateCompetenciesContent(langData);
}

