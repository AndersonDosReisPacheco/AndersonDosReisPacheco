function saveLanguagePreference(lang) {
  try {
    localStorage.setItem("portfolio-language", lang);
  } catch (e) {
    console.log("Could not save language preference:", e);
  }
}

function loadLanguagePreference() {
  try {
    const savedLang = localStorage.getItem("portfolio-language");
    if (savedLang && translations[savedLang]) {
      currentLanguage = savedLang;
      return;
    }
  } catch (e) {
    console.log("Could not load language preference:", e);
  }

  // Detect browser language
  const browserLang = navigator.language || navigator.userLanguage;
  if (browserLang.startsWith("pt")) {
    currentLanguage = "pt-BR";
  } else if (browserLang.startsWith("en")) {
    currentLanguage = browserLang.includes("GB") ? "en-GB" : "en-US";
  }
}

function updateActiveLanguageButton() {
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    const lang = btn.getAttribute("data-lang");
    if (lang === currentLanguage) {
      btn.classList.add("active");
      btn.setAttribute("aria-current", "true");
    } else {
      btn.classList.remove("active");
      btn.removeAttribute("aria-current");
    }
  });

  // Update language toggle text
  const toggleSpan = document.querySelector(".language-toggle span");
  if (toggleSpan) {
    toggleSpan.textContent =
      currentLanguage === "pt-BR"
        ? "PT"
        : currentLanguage === "en-US"
          ? "EN-US"
          : "EN-UK";
  }
}

function updateURLWithLanguage() {
  if (history.pushState) {
    const url = new URL(window.location);
    url.searchParams.set("lang", currentLanguage);
    history.pushState(null, "", url.toString());
  }
}

// Initialize language on page load
document.addEventListener("DOMContentLoaded", function () {
  // Check URL for language parameter
  const urlParams = new URLSearchParams(window.location.search);
  const urlLang = urlParams.get("lang");
  if (urlLang && translations[urlLang]) {
    currentLanguage = urlLang;
  } else {
    loadLanguagePreference();
  }

  // Add language selector to navbar
  addLanguageSelector();

  // Update texts after DOM is fully loaded
  setTimeout(() => {
    updateAllTexts();
    updateActiveLanguageButton();
  }, 300);
});

function addLanguageSelector() {
  const navbar = document.querySelector(".navbar");
  if (!navbar) return;

  // Check if language selector already exists
  if (document.querySelector(".language-selector")) return;

  const languageSelector = document.createElement("div");
  languageSelector.className = "language-selector";
  languageSelector.innerHTML = `
      <div class="language-dropdown">
        <button class="language-toggle" id="language-toggle" aria-label="Select language" aria-haspopup="true" aria-expanded="false">
          <i class="fas fa-globe" aria-hidden="true"></i>
          <span>${currentLanguage === "pt-BR" ? "PT" : currentLanguage === "en-US" ? "EN-US" : "EN-UK"}</span>
          <i class="fas fa-chevron-down" aria-hidden="true"></i>
        </button>
        <div class="language-menu" role="menu" aria-labelledby="language-toggle">
          <button class="lang-btn" data-lang="pt-BR" role="menuitem" aria-label="Portuguese (Brazil)">
            <span class="flag-icon">🇧🇷</span> Português (BR)
          </button>
          <button class="lang-btn" data-lang="en-US" role="menuitem" aria-label="English (US)">
            <span class="flag-icon">🇺🇸</span> English (US)
          </button>
          <button class="lang-btn" data-lang="en-GB" role="menuitem" aria-label="English (UK)">
            <span class="flag-icon">🇬🇧</span> English (UK)
          </button>
        </div>
      </div>
    `;

  // Insert before theme toggle
  const themeToggle = document.querySelector(".theme-toggle");
  if (themeToggle) {
    themeToggle.parentNode.insertBefore(languageSelector, themeToggle);
  } else {
    navbar.appendChild(languageSelector);
  }

  // Add event listeners
  document
    .getElementById("language-toggle")
    ?.addEventListener("click", function (e) {
      e.stopPropagation();
      const isExpanded = this.getAttribute("aria-expanded") === "true";
      this.setAttribute("aria-expanded", !isExpanded);
      this.parentElement.classList.toggle("active");
    });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", function (e) {
      e.stopPropagation();
      const lang = this.getAttribute("data-lang");
      changeLanguage(lang);

      // Close dropdown
      document.querySelector(".language-dropdown").classList.remove("active");
      document
        .getElementById("language-toggle")
        .setAttribute("aria-expanded", "false");

      // Announce language change for accessibility
      const langNames = {
        "pt-BR": "Portuguese",
        "en-US": "English US",
        "en-GB": "English UK",
      };
      announceScreenReader(`Language changed to ${langNames[lang]}`);
    });
  });

  // Close dropdown when clicking outside
  document.addEventListener("click", function (event) {
    const dropdown = document.querySelector(".language-dropdown");
    const toggle = document.getElementById("language-toggle");
    if (dropdown && !dropdown.contains(event.target)) {
      dropdown.classList.remove("active");
      if (toggle) toggle.setAttribute("aria-expanded", "false");
    }
  });

  // Close dropdown on escape key
  document.addEventListener("keydown", function (e) {
    if (e.key === "Escape") {
      const dropdown = document.querySelector(".language-dropdown");
      const toggle = document.getElementById("language-toggle");
      if (dropdown && dropdown.classList.contains("active")) {
        dropdown.classList.remove("active");
        if (toggle) toggle.setAttribute("aria-expanded", "false");
        toggle?.focus();
      }
    }
  });
}

// Accessibility function for screen readers
function announceScreenReader(message) {
  const announcement = document.createElement("div");
  announcement.setAttribute("aria-live", "polite");
  announcement.setAttribute("aria-atomic", "true");
  announcement.classList.add("sr-only");
  announcement.textContent = message;

  document.body.appendChild(announcement);

  setTimeout(() => {
    document.body.removeChild(announcement);
  }, 1000);
}

